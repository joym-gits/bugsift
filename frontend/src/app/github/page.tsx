function InstallationsSection({
  loading,
  installations,
  appSlug,
}: {
  loading: boolean;
  installations: InstallationOut[];
  appSlug: string | null;
}) {
  const installUrl = appSlug
    ? `https://github.com/apps/${appSlug}/installations/new`
    : null;
  const hydrate = useHydrateRepos();
  const [hydrateMessage, setHydrateMessage] = useState<string | null>(null);
  const [hydrateError, setHydrateError] = useState<string | null>(null);

  return (
    <section className="rounded-lg border bg-card p-6 shadow-sm">
      <div className="mb-1 flex items-center justify-between gap-2">
        <div className="flex items-center gap-2">
          <KeyRound className="h-4 w-4 text-muted-foreground" />
          <h2 className="text-base font-medium">Installations</h2>
        </div>
        <div className="flex gap-2">
          <button
            type="button"
            onClick={async () => {
              setHydrateMessage(null);
              setHydrateError(null);
              try {
                const result: HydrateResult = await hydrate.mutateAsync();
                setHydrateMessage(
                  `Re-synced ${result.installations} installation${
                    result.installations === 1 ? "" : "s"
                  } — ${result.added} new repo${result.added === 1 ? "" : "s"}, ${result.skipped} already present.`,
                );
              } catch (e) {
                setHydrateError(
                  e instanceof ApiError
                    ? e.message
                    : e instanceof Error
                      ? e.message
                      : "hydrate failed",
                );
              }
            }}
            disabled={hydrate.isPending}
            className="inline-flex h-8 items-center gap-1.5 rounded-md border border-input bg-background px-3 text-xs font-medium hover:bg-accent hover:text-accent-foreground disabled:opacity-50"
          >
            {hydrate.isPending ? "syncing…" : "Re-sync repos"}
          </button>
          {installUrl && (
            <a
              href={installUrl}
              target="_blank"
              rel="noopener noreferrer"
              className="inline-flex h-8 items-center gap-1.5 rounded-md border border-input bg-background px-3 text-xs font-medium hover:bg-accent hover:text-accent-foreground"
            >
              Install on another repo <ExternalLink className="h-3 w-3" />
            </a>
          )}
        </div>
      </div>
      <p className="text-sm text-muted-foreground">
        Each installation binds bugsift to a repo you own or maintain. Use
        <strong> Re-sync repos</strong> if GitHub&apos;s install webhook
        arrived without its repo list (a known GitHub quirk) — bugsift will
        query the API and backfill.
      </p>
      {hydrateMessage && (
        <p className="mt-2 rounded-md border border-primary/30 bg-primary/5 px-3 py-1.5 text-xs text-primary">
          {hydrateMessage}
        </p>
      )}
      {hydrateError && (
        <p className="mt-2 rounded-md border border-destructive/30 bg-destructive/5 px-3 py-1.5 text-xs text-destructive">
          {hydrateError}
        </p>
      )}

      {loading ? (
        <Skeleton className="mt-4 h-16 w-full" />
      ) : installations.length === 0 ? (
        <p className="mt-4 text-sm text-muted-foreground">
          No installations yet. Install the App on a repo to start triaging.
        </p>
      ) : (
        <ul className="mt-4 divide-y">
          {installations.map((inst) => (
            <li key={inst.id} className="flex items-center justify-between py-3 text-sm">
              <div>
                <div className="font-mono text-xs text-muted-foreground">
                  installation #{inst.github_installation_id}
                </div>
                <div className="mt-0.5">
                  {inst.repo_count} repo{inst.repo_count === 1 ? "" : "s"} · installed{" "}
                  {new Date(inst.installed_at).toLocaleDateString()}
                  {inst.suspended_at && (
                    <span className="ml-2 rounded-full border border-amber-500/40 bg-amber-500/10 px-2 py-0.5 text-xs font-medium text-amber-700">
                      suspended
                    </span>
                  )}
                </div>
              </div>
              {appSlug && (
                <a
                  href={`https://github.com/settings/installations/${inst.github_installation_id}`}
                  target="_blank"
                  rel="noopener noreferrer"
                  className="text-xs underline underline-offset-4"
                >
                  Manage on GitHub
                </a>
              )}
            </li>
          ))}
        </ul>
      )}
    </section>
  );
}

function ReposSection({ signedIn }: { signedIn: boolean }) {
  const repos = useRepos(signedIn);
  const backfill = useBackfillRepo();
  const [status, setStatus] = useState<{
    repoId: number;
    kind: "ok" | "err";
    message: string;
  } | null>(null);

  return (
    <section className="rounded-lg border bg-card p-6 shadow-sm">
      <div className="mb-1 flex items-center gap-2">
        <Inbox className="h-4 w-4 text-muted-foreground" />
        <h2 className="text-base font-medium">Synced repos</h2>
      </div>
      <p className="text-sm text-muted-foreground">
        When bugsift adds a repo, it automatically pulls every open issue and
        runs them through triage so the dashboard reflects the current state.
        Use <strong>Triage existing issues</strong> to re-run that pass on
        demand &mdash; useful if a repo was attached before the App config was
        finalised, or to pick up issues opened while the tunnel was offline.
      </p>

      {repos.isLoading ? (
        <Skeleton className="mt-4 h-16 w-full" />
      ) : (repos.data ?? []).length === 0 ? (
        <p className="mt-4 text-sm text-muted-foreground">
          No repos synced yet. Install the App on a repo, or click{" "}
          <em>Re-sync repos</em> above if an install event arrived empty.
        </p>
      ) : (
        <ul className="mt-4 divide-y">
          {(repos.data ?? []).map((repo: Repo) => (
            <li
              key={repo.id}
              className="flex items-center justify-between gap-4 py-3 text-sm"
            >
              <div className="min-w-0">
                <div className="truncate font-medium">{repo.full_name}</div>
                <div className="mt-0.5 text-xs text-muted-foreground">
                  {repo.primary_language ?? "unknown language"} ·{" "}
                  {repo.default_branch} · indexing:{" "}
                  <span className="font-mono">{repo.indexing_status}</span>
                </div>
                {status?.repoId === repo.id && (
                  <div
                    className={
                      "mt-1 text-xs " +
                      (status.kind === "ok"
                        ? "text-primary"
                        : "text-destructive")
                    }
                  >
                    {status.message}
                  </div>
                )}
              </div>
              <button
                type="button"
                onClick={async () => {
                  setStatus(null);
                  try {
                    await backfill.mutateAsync(repo.id);
                    setStatus({
                      repoId: repo.id,
                      kind: "ok",
                      message:
                        "Queued. Existing open issues will appear in the Queue as triage completes.",
                    });
                  } catch (e) {
                    setStatus({
                      repoId: repo.id,
                      kind: "err",
                      message:
                        e instanceof ApiError
                          ? e.message
                          : e instanceof Error
                            ? e.message
                            : "backfill failed",
                    });
                  }
                }}
                disabled={backfill.isPending}
                className="shrink-0 inline-flex h-8 items-center gap-1.5 rounded-md border border-input bg-background px-3 text-xs font-medium hover:bg-accent hover:text-accent-foreground disabled:opacity-50"
              >
                {backfill.isPending && backfill.variables === repo.id
                  ? "queueing…"
                  : "Triage existing issues"}
              </button>
            </li>
          ))}
        </ul>
      )}
    </section>
  );
}

function DangerZone({
  details,
  confirming,
  onToggle,
  onConfirm,
  pending,
  error,
}: {
  details: AppDetails;
  confirming: boolean;
  onToggle: () => void;
  onConfirm: () => void;
  pending: boolean;
  error: string | null;
}) {
  return (
    <section className="rounded-lg border border-destructive/30 bg-destructive/5 p-6">
      <div className="mb-1 flex items-center gap-2">
        <RotateCcw className="h-4 w-4 text-destructive" />
        <h2 className="text-base font-medium text-destructive">
          Re-register the App
        </h2>
      </div>
      <p className="text-sm text-muted-foreground">
        Removes the stored credentials (App ID, client secret, webhook secret,
        private key) and starts the onboarding wizard. Your existing App on
        github.com is <strong>not</strong> deleted &mdash; you can either install
        the new App side-by-side, or delete the old one from{" "}
        {details.html_url ? (
          <a
            href={`${details.html_url}/advanced`}
            target="_blank"
            rel="noopener noreferrer"
            className="underline underline-offset-4"
          >
            its GitHub settings
          </a>
        ) : (
          "its GitHub settings"
        )}
        . Installations already linked will stop receiving events.
      </p>

      {confirming ? (
        <div className="mt-4 space-y-3 rounded-md border border-destructive/30 bg-background/60 p-4 text-sm">
          <div className="font-medium">Confirm removal</div>
          <div className="text-muted-foreground">
            This will wipe the App credentials from bugsift and send you to
            onboarding Step 1. Continue?
          </div>
          {error && (
            <div className="rounded-md border border-destructive/40 bg-destructive/10 px-3 py-2 text-destructive">
              {error}
            </div>
          )}
          <div className="flex gap-2">
            <Button
              size="sm"
              variant="destructive"
              onClick={onConfirm}
              disabled={pending}
            >
              {pending ? "removing…" : "Yes, remove App"}
            </Button>
            <Button size="sm" variant="outline" onClick={onToggle}>
              Cancel
            </Button>
          </div>
        </div>
      ) : (
        <div className="mt-4">
          <Button variant="outline" onClick={onToggle}>
            <RefreshCw className="mr-2 h-4 w-4" />
            Remove and re-register
          </Button>
        </div>
      )}
    </section>
  );
}

function Field({
  label,
  value,
  mono = false,
  hint,
}: {
  label: string;
  value: string | null | undefined;
  mono?: boolean;
  hint?: string;
}) {
  return (
    <div>
      <dt className="text-xs font-medium uppercase tracking-wide text-muted-foreground">
        {label}
      </dt>
      <dd className={"mt-1 " + (mono ? "font-mono text-xs break-all" : "text-sm")}>
        {value || <span className="text-muted-foreground">—</span>}
      </dd>
      {hint && <p className="mt-1 text-xs text-muted-foreground">{hint}</p>}
    </div>
  );
}
