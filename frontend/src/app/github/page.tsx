"use client";

import Link from "next/link";
import { useState } from "react";
import {
  AlertTriangle,
  Check,
  ExternalLink,
  Github,
  Inbox,
  KeyRound,
  RefreshCw,
  RotateCcw,
  Webhook,
} from "lucide-react";

import { AppShell, EmptyState, PageHeader, Skeleton } from "@/components/AppShell";
import { Button } from "@/components/ui/button";
import { ApiError } from "@/lib/api";
import {
  type AppDetails,
  type HydrateResult,
  type InstallationOut,
  type Repo,
  useAppDetails,
  useBackfillRepo,
  useDeleteApp,
  useHydrateRepos,
  useInstallations,
  useMe,
  useRepos,
} from "@/lib/hooks";

export default function GithubSettingsPage() {
  const me = useMe();
  const signedIn = Boolean(me.data);
  const details = useAppDetails(signedIn);
  const installations = useInstallations(signedIn);
  const deleteApp = useDeleteApp();
  const [confirmDelete, setConfirmDelete] = useState(false);
  const [error, setError] = useState<string | null>(null);

  return (
    <AppShell me={me.data ?? null}>
      {!signedIn ? (
        <EmptyState
          icon={Github}
          title="Sign in to view GitHub configuration"
          description="This page shows the registered App, stored secrets (masked), and installations attached to your account."
        />
      ) : (
        <>
          <PageHeader
            title="GitHub configuration"
            description="Every value bugsift stored during onboarding. Secrets are masked — bugsift never displays them in plaintext."
          />

          {details.isLoading || !details.data ? (
            <Skeleton className="h-64 w-full" />
          ) : !details.data.configured ? (
            <NotConfigured />
          ) : (
            <div className="space-y-6">
              <AppSection details={details.data} />
              <TunnelSection details={details.data} />
              <InstallationsSection
                loading={installations.isLoading}
                installations={installations.data ?? []}
                appSlug={details.data.slug ?? null}
              />
              <ReposSection signedIn={signedIn} />
              <DangerZone
                details={details.data}
                confirming={confirmDelete}
                onToggle={() => {
                  setError(null);
                  setConfirmDelete((v) => !v);
                }}
                onConfirm={async () => {
                  try {
                    await deleteApp.mutateAsync();
                    setConfirmDelete(false);
                    window.location.assign("/onboarding?step=app");
                  } catch (e) {
                    if (e instanceof ApiError) setError(e.message);
                    else if (e instanceof Error) setError(e.message);
                    else setError("failed to remove App");
                  }
                }}
                pending={deleteApp.isPending}
                error={error}
              />
            </div>
          )}
        </>
      )}
    </AppShell>
  );
}

